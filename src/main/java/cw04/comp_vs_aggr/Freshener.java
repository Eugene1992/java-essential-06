package cw04.comp_vs_aggr;

public class Freshener {
    private String model;
    private String smell;

    public Freshener(String model, String smell) {
        this.model = model;
        this.smell = smell;
    }

    public String getModel() {
        return model;
    }

    public String getSmell() {
        return smell;
    }
}
